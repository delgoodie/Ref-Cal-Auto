# Returns the file extension
def FileType(path: str) -> str:
    match = re.match("^.*\\.(\\w+)$", path)
    if len(match.regs) > 1:
        return path[match.regs[-1][0] : match.regs[-1][1]]
    else:
        print("ERROR [" + path + "] has no File Type")
        return ""


